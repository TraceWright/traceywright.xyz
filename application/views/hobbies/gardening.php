	<br><br>
		
	During summer of 2014, I got some treated timber (the non-toxic kind) from my local timber supplier, and put together this vege box. I had some help from a friend with a drill for the assembly, but the rest was all me. I dug up some of the backyard soil to give the vege roots the best possible chance of working their way through the deeper soil, then loaded in a trailer-full of soil. After mixing through a few bags of cow poo to give the soil some extra micro-nutrients, my seedlings went in. 

	<br><br>

	As you can see, the tomato plants started out as teeny tiny seedlings. Before long, I had to get some wire framing in to support the branches and their heavy tomatoes. The yield was good, and tasted absolutely delicious! There's nothing like freshly picked veg that's gone straight from garden to plate. Made me realise how tasteless the supermarket food is. In addition to the tomatoes and zucchinis pictured, I had squash, cabbage, capsicum, lettuce, beetroot, corn, and more!

