<p>
	At the beginning of Spring in 2014, I decided that I wanted to give some time to being a 'green thumb'. It was all triggered by a bargain pick-up of some potted flowers at my local Woolies. They had a whole bunch of potted daisies that were close to their end, so they were selling them off cheap. I took home a couple of dozen of them and brought them back to their former glory with a little TLC. I then decided to give them a permanent home in a garden I created in our back yard. That's how it all began!

	<br><br>

	Pictured right is one of my favourite roses which I owned long before I created all the gardens you'll see in the galleries below. This, and a couple of other colour varieties, was a gift that I received one Valentine's Day. I adore this colour. Peaches are one of my favourite fruits and the colour of this rose reminds me of delicious peach. 

	<br><br>

	Coming home to beautiful flowers like this, then spending half an hour standing out with the hose watering everything and finding new blossoms along the way is a great way to wind down at the end of a day.


</p>


