<p>

	The general purpose of a Mohs tech is to process skin excisions from surgical procedures while the patient is on the table. The benefit of this is that a minimal amount of tissue may be excised, and if required further tissue may be excised if the surgeon has determined that the margins of the excision aren't clear of pathology. My role involved embedding pieces of the excision into a mould, then covering the excision in a viscous compound, and rapidly freezing until the compound became a frozen solid. I then removed the mould, and cut extremely thin sections of the compound-embedded excision, and placed these sections onto slides. The slides were then processed through a series of liquid reagents and stains which basically provides colour differentiation of cell types. I then glued thin, glass coverslips over the stained section on the slide, and passed the completed slides onto the surgeon for viewing under the microscope.

	
</p>
