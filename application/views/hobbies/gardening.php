	<p>

	<br><br>
		
	The aforementioned daisies make their way into the backyard ground, then a whole bunch of other plants from my local Bunnings followed. Making this happen required a little bit of hard work with a shovel, but I thoroughly enjoyed the process, especially when the flowers came into bloom and brought beautiful, vivid colours into my yard.

	<br><br>

	The old tree trunk stub served as a pot of sorts for the first daisy plant, which I thought was a great way to bring new life to something old. Some time after I planted the daisy, little mushrooms began to grow around the base of the trunk! So cute! Before long, I had the area along the fence full. I lined the garden with some timber from our firewood stash, and got in some mulch to protect the flowers from the upcoming summer heat. A touch of wall art and some solar lights finished up the garden work, and I would sit by the BBQ in the evenings looking at the pretty flowers whilst I cooked dinner enjoying the view of my colourful yard.

	<br><br>

	</p>

